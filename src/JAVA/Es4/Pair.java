
class Pair{
    private int adj;
    private int weight;

    public Pair(int adj, int weight){
        this.adj = adj;
        this.weight = weight;
    }

    public int getAdj(){
        return adj;
    }
    public void setAdj(int adj){
        this.adj = adj;
    }
    public int getWeight(){
        return weight;
    }
    public void setWeight(int weight){
        this.weight = weight;
    }


}
